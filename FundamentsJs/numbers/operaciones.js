// **OPERACIONES BASICAS** //

const number1 = 30;
const number2 = 40;
let resultado;

// * Suma
// Los datos al ser numeros y utilizar el operador + no se concatenan como ocurre con los strings, en su lugar se realiza la operacion aritmetica 
resultado = number1 + number2; 
console.log(resultado);

// * Resta
resultado = number1 - number2;
console.log(resultado);

// * Multiplicacion
resultado = number1 * number2;
console.log(resultado);

// * Division
resultado = number1 / number2;
console.log(resultado);

// * Modulo
resultado = number1 % number2;
console.log(resultado);
